''' Operation with Strings '''

""" 1. Length of a string """

str1 = "Hello"
print(len(str1)) # Output: 5

""" 2. String as an array"""

str2 = "Hello"
print(str2[1:5]) # Output: ello

""" 3. Loop through a string"""

for i in str1:
    print(i)  # Output:
              # H
              # e
              # l
              # l
              # o

